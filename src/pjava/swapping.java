package pjava;

public class swapping {
    public static void main(String[] args) {

        int a = 30;
        int b = 40;

        System.out.println("swapping a" + a);
        System.out.println("swapping b"+ b);


        int temp = a;
        a=b;
        b=temp;

        System.out.println("temp wapping "+a);
        System.out.println("temp swapping "+b);



    }
}
